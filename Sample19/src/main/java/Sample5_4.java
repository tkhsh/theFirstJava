public class Sample5_4 {
	public static void main(String[] args) {
		int fatherAge = 52;
		int motherAge = 50;
		int myAge = 22;
		int sisterAge = 20;
		int sumAge = fatherAge + motherAge + myAge + sisterAge;
		int averageAge = sumAge / 4;

		System.out.println("家族の平均年齢は、" + averageAge + "歳です。");
	}
}
